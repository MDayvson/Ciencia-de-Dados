# Vamos criar uma variável nota com o valor 5
nota <- 6

# Verificando se nota é maior que 5
nota >= 5

# Verificando se nota é igual a 6
nota == 6

# Criando uma nova variável presença com valor 77
presenca = 71

# Verificando se nota é maior que 5 E presença é maior que 70
nota > 5 & presenca > 70

# Verificando se a nota é menor que 3 OU presença menor que 50
nota < 3 | presenca < 50

# Verificando se nota > 9 OU 
#    se nota > 5 E presença > 70
nota > 9 | (nota > 5 & presenca > 70)
#   F     |    ( V     &    V)
#   F     |            V
#          V